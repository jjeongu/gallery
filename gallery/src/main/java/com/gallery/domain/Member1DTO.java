package com.gallery.domain;

public class Member1DTO {
	private String memeber_id;
	private String memeber_pwd;
	private String birth;
	private String tel;
	private String email;
	private String role;
	private String name;
	
	
	
	public String getMemeber_id() {
		return memeber_id;
	}
	public void setMemeber_id(String memeber_id) {
		this.memeber_id = memeber_id;
	}
	public String getMemeber_pwd() {
		return memeber_pwd;
	}
	public void setMemeber_pwd(String memeber_pwd) {
		this.memeber_pwd = memeber_pwd;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
